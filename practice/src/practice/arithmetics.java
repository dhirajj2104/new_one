package practice;

public class arithmetics {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		System.out.println("Arithmetic operations : ");
		System.out.println("Addition : 10 + 20 : "+(a+b));
		System.out.println("Substraction : 10 - 20 : "+(a-b));
		System.out.println("Multiplication : 10 * 20 : "+(a*b));
		System.out.println("Division : 20 / 10 : "+(b/a));
	}
}
